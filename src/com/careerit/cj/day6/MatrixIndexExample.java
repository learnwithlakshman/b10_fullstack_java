package com.careerit.cj.day6;

public class MatrixIndexExample {

		public static void main(String[] args) {
			
				int r = 5;
				int c = 3;
				
				for(int i=0;i<r;i++) {
					
					for(int j=0;j<c;j++) {
						System.out.print(i+""+j+" ");
					}
					System.out.println();
					
				}
				
				
		}
}
