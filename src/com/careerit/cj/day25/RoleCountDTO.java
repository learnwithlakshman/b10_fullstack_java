package com.careerit.cj.day25;

public class RoleCountDTO {

		private String role;
		private int count;
		
		public RoleCountDTO(String role, int count) {
			this.role = role;
			this.count = count;
		}

		public String getRole() {
			return role;
		}

		public void setRole(String role) {
			this.role = role;
		}

		public int getCount() {
			return count;
		}

		public void setCount(int count) {
			this.count = count;
		}
		
		
		
		
		
		
}
